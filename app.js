app.get("/post/:postID", function(req, res){ // Declare the route params
  //res.send(req.params);
  let postIndex = req.params.postID;
  res.render("post", {
    thisPost: PostArray[postIndex]
  })
})

app.post("/compose", function(req, res){
  newPostTitle = req.body.newPostTitle;
  newPostContent = req.body.newPostDescription;
  const Post = {
    Title: newPostTitle,
    Content: newPostContent
  }
  PostArray.push(Post);
  PostArrayLength = PostArray.length;
  res.redirect("/");
})














